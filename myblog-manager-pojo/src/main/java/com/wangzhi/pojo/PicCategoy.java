package com.wangzhi.pojo;

public class PicCategoy {
    private Integer picCategoryId;
    private String picCategoryName;
    private String picCategoryDescription;
    private String picCategoryIcon;
    private Integer picCategoryStatus;

    public Integer getPicCategoryId() {
        return picCategoryId;
    }

    public void setPicCategoryId(Integer picCategoryId) {
        this.picCategoryId = picCategoryId;
    }

    public String getPicCategoryName() {
        return picCategoryName;
    }

    public void setPicCategoryName(String picCategoryName) {
        this.picCategoryName = picCategoryName;
    }

    public String getPicCategoryDescription() {
        return picCategoryDescription;
    }

    public void setPicCategoryDescription(String picCategoryDescription) {
        this.picCategoryDescription = picCategoryDescription;
    }

    public String getPicCategoryIcon() {
        return picCategoryIcon;
    }

    public void setPicCategoryIcon(String picCategoryIcon) {
        this.picCategoryIcon = picCategoryIcon;
    }

    public Integer getPicCategoryStatus() {
        return picCategoryStatus;
    }

    public void setPicCategoryStatus(Integer picCategoryStatus) {
        this.picCategoryStatus = picCategoryStatus;
    }
}
